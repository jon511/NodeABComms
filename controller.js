function intTo2BytesLittleEndian(val) {
    let b = []
    b[0] = val & 0x000000ff
    b[1] = (val & 0x0000ff00) >> 8

    return b
}

function intTo4BytesLittleEndian(val) {
    let b = []
    b[0] = val & 0x000000ff
    b[1] = (val & 0x0000ff00) >> 8
    b[2] = (val & 0x00ff0000) >> 16
    b[3] = (val & 0x7f000000) >> 24

    return b
}

